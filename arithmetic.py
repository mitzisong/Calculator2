def add(first, second):
    return first + second

def subtract(first, second):
    return first - second

def multiply(first, second):
    return first * second

def divide(first, second):
    return first / second

def square(i):
    return i**2

def cube(i):
    return i**3

def mod(i, j):
    return i%j

def power(i, j):
    return i**j
